package pila;

/**
 *
 * @author danyc
 */
public class Nodo {

    String info;
    Nodo sig;

    public Nodo(String info) {
        this.info = info;
    }

    public String getInfo() {
        return info;
    }

    public void setInfo(String info) {
        this.info = info;
    }

    public Nodo getSig() {
        return sig;
    }

    public void setSig(Nodo sig) {
        this.sig = sig;
    }

}
